#include "libft.h"

void	*ft_memcpy(void *dst, const void *src, size_t n)
{
	unsigned char	*d;
	unsigned char	*s;

	d = (unsigned char *)dst;
	s = (unsigned char *)src;
	if (dst == src)
		return (dst);
	while (d < ((unsigned char *)dst + n))
		*d++ = *(unsigned char *)s++;
	return (dst);
}
